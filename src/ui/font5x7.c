#define N_GLYPHS (sizeof(s_font) / sizeof(s_font[0]))

bool font5x7_get(char c, uint8_t out[FONT5X7_HEIGHT]) {
    if (c >= 'a' && c <= 'z') c = (char)(c - 'a' + 'A');
    for (size_t i = 0; i < N_GLYPHS; ++i) {
        if (s_font[i].c == c) {
            for (int r = 0; r < FONT5X7_HEIGHT; ++r) out[r] = s_font[i].rows[r];
            return true;
        }
    }
    return false;
}
